    @Test
    public void testQValues() {
        QValues = new double[noOfStates][noOfActions];
        for (int i = 1; i < noOfStates; i++) {
            for (int j = 0; j < noOfActions; j++) {
                QValues[i][j] = 0;
            }
        }
        getExperimentData();

        for (int i = 1; i < 1000; i++) {
            System.out.println(values[i][0] + "\t" + values[i][1] + "\t" + values[i][2]);
            updatePolicy(i);
        }

        printPolicy();
    }

    protected void updatePolicy(int i) {
        double learningRate = (200.0 / (300.0 + i));
        double currentQ = QValues[values[i][0] + offset][values[i][1] + offset];
        double transitionQ = getMaxRewardForState(i + 1);
        double updatedQValue =
                (1.0 - learningRate) * currentQ + learningRate * (values[i][2] + discountFactor * transitionQ);
        QValues[values[i][0] + offset][values[i][1] + offset] = updatedQValue;
//        System.out.print(" time step:" + 1);
//        System.out.print(" learningRate:" + df.format(learningRate));
//        System.out.print(" Change Q value:" + df.format(currentQ));
//        System.out.print(" to " + df.format(updatedQValue));
//        System.out.println("");
    }

    private double getMaxRewardForState(int i) {
        double bestValue = 0;
        for (int j = 0; j < noOfActions; j++) {
            if (QValues[values[i][0] + offset][j] >= bestValue) {
                bestValue = QValues[values[i][0] + offset][j];
            }
        }
        return bestValue;
    }

    public void printPolicy() {
        for (int i = 0; i < noOfStates; i++) {
            System.out.print((i - offset) + "  \t");
        }
        System.out.println("");
        for (int i = 0; i < noOfStates; i++) {
            if ((i - offset) == 5 || (i - offset) == -5) {
                System.out.print((i - offset) + "\t");
                for (int j = 0; j < noOfActions; j++) {
                    System.out.print(df.format(QValues[i][j]) + "\t");
                }
                System.out.println("");
            }
        }
    }

